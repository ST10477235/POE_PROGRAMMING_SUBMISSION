    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String logUsername = logUsernameField.getText();
        String logPassword = new String(logPasswordField.getPassword());

        // Reset error messages
        loginUsernameErrorLabel.setVisible(false);
        loginPasswordErrorLabel.setVisible(false);

        // Check if fields are empty and display error messages
        boolean hasError = false;
        if (logUsername.isEmpty()) {
            loginUsernameErrorLabel.setText("Username cannot be empty!");
            loginUsernameErrorLabel.setForeground(Color.RED);
            loginUsernameErrorLabel.setVisible(true);
            hasError = true;
        }
        if (logPassword.isEmpty()) {
            loginPasswordErrorLabel.setText("Password cannot be empty!");
            loginPasswordErrorLabel.setForeground(Color.RED);
            loginPasswordErrorLabel.setVisible(true);
            hasError = true;
        }

        if (hasError) {
            return; // Stop execution if fields are empty
        }
        
   
        String[] userInfo = Login.users.get(logUsername);
if (userInfo == null) {
    System.out.println("User not found in map! Login attempt failed.");
} else {
    System.out.println("User found: " + userInfo[0] + " | Stored password: " + userInfo[1]);
    System.out.println("Entered password: " + logPassword);
}

        // Validate login using LoggingUser method
        boolean validLogin = Login.LoggingUser(logUsername, logPassword, Login.users); 
        JOptionPane.showMessageDialog(null, validLogin ? "ACCESS GRANTED" : "ACCESS DENIED", 
    "Login Status", validLogin ? JOptionPane.INFORMATION_MESSAGE : JOptionPane.ERROR_MESSAGE);
        if (validLogin) {
        dispose();
        } 
        Login.isAuthenticated = validLogin;
    }//GEN-LAST:event_jButton2ActionPerformed

